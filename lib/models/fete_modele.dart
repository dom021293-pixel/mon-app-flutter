class Fete {
  final int jour;
  final int mois;
  final String nom;
  final String description;
  final String type; // "fete", "solennite", "memoire", "temps"
  final String couleur; // couleur liturgique
  final String? saintDuJour;
  final String? imageUrl;

  Fete({
    required this.jour,
    required this.mois,
    required this.nom,
    required this.description,
    required this.type,
    required this.couleur,
    this.saintDuJour,
    this.imageUrl,
  });

  DateTime get date {
    final now = DateTime.now();
    return DateTime(now.year, mois, jour);
  }

  bool get estPasse {
    final now = DateTime.now();
    final dateFete = date;
    return now.isAfter(dateFete);
  }

  int get joursRestants {
    final now = DateTime.now();
    final dateFete = date;
    if (now.isAfter(dateFete)) {
      final anneeProchaine = now.year + 1;
      return DateTime(anneeProchaine, mois, jour).difference(now).inDays;
    }
    return dateFete.difference(now).inDays;
  }
}

