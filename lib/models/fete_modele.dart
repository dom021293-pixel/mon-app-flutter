  static List<Fete> getFetesDuJour(DateTime date) {
    return toutesLesFetes.where((fete) =>
      fete.jour == date.day && fete.mois == date.month
    ).toList();
  }

  static List<Fete> getFetesDuMois(int mois) {
    return toutesLesFetes.where((fete) => fete.mois == mois).toList();
  }

  static Fete? getFeteDuSaintJoseph() {
    try {
      return toutesLesFetes.firstWhere((fete) =>
        fete.saintDuJour == 'Joseph'
      );
    } catch (e) {
      return null;
    }
  }
